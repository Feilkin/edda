//! token definitions

#[derive(Debug, Eq, PartialEq)]
pub struct Token<'s> {
    pub t_type: TokenType,
    pub offset: usize,
    pub text: &'s str,
}

#[derive(Debug, Eq, PartialEq)]
pub enum TokenType {
    Number,
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    Comma,
    Dot,
    DotDot,
    Semicolon,
    Plus,
    Minus,
    Star,
    Slash,
    LineComment,
    Caret,
    Bang,
    BangEqual,
    Equal,
    PlusEqual,
    MinusEqual,
    StarEqual,
    SlashEqual,
    EqualEqual,
    LessEqual,
    GreaterEqual,
    Less,
    Greater,
    Arrow,
    FatArrow,
    Identifier,
    Let,
    And,
    Or,
    For,
    In,
    While,
    Print,
    True,
    False,
    If,
    Else,
    Return,
}
